def generate_subject_questions(subject, count=100000):
    """为单个科目生成题目"""
    questions = []
    print(f"开始生成 {SUBJECTS[subject]} 题库，目标数量: {count}")
    
    single_count = int(count * 0.5)
    multiple_count = int(count * 0.3)
    judge_count = count - single_count - multiple_count
    
    start_time = time.time()
    
    for i in range(single_count):
        questions.append(generate_single_question(subject, 'single', i))
        if (i + 1) % 10000 == 0:
            elapsed = time.time() - start_time
            print(f"  已生成单选题: {i + 1}/{single_count} ({elapsed:.1f}s)")
    
    for i in range(multiple_count):
        questions.append(generate_single_question(subject, 'multiple', i))
        if (i + 1) % 10000 == 0:
            elapsed = time.time() - start_time
            print(f"  已生成多选题: {i + 1}/{multiple_count} ({elapsed:.1f}s)")
    
    for i in range(judge_count):
        questions.append(generate_single_question(subject, 'judge', i))
        if (i + 1) % 10000 == 0:
            elapsed = time.time() - start_time
            print(f"  已生成判断题: {i + 1}/{judge_count} ({elapsed:.1f}s)")
    
    elapsed = time.time() - start_time
    print(f"{SUBJECTS[subject]} 题库生成完成，共 {len(questions)} 道题目，耗时 {elapsed:.1f}s")
    
    return questions


